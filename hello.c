#include <stdio.h>

int main() {
    char name[100]; // Массив для хранения имени

    // Запрос имени у пользователя
    printf("Введите ваше имя: ");
    scanf("%99s", name); // Ограничение на ввод

    // Приветствие с восклицательным знаком
    printf("hello, %s\n", name); // Убедитесь, что здесь только \n, без !

    return 0;
}
